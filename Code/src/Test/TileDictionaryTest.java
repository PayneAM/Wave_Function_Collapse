    private String[] GetFileNameOfTiles(Tile[] testTile) {
        Stream<Object> testTileEdgeList = Arrays.stream(testTile).map(tile -> tile.imgFile.getName());
        String[] array = testTileEdgeList.toArray(String[]::new);
        return array;
    }
    private String[][] GetImgEdgeOfTiles(Tile[] testTile) {
        Stream<Object> testTileEdgeList = Arrays.stream(testTile).map(tile -> tile.imgEdges);
        String[][] array = testTileEdgeList.toArray(String[][]::new);
        return array;
    }
    private int[][][] GetCompatibleOfTiles(Tile[] testTile) {
        Stream<Object> testTileEdgeList = Arrays.stream(testTile).map(tile -> tile.compatibleTiles);
        int[][][] array = testTileEdgeList.toArray(int[][][]::new);
        return array;
    }

